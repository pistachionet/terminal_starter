-- Autosave on leaving insert mode (VS Code-like)
-- Remove this block if you don't want it
vim.api.nvim_create_autocmd("InsertLeave", {
	desc = "Autosave when leaving insert mode",
	group = vim.api.nvim_create_augroup("autosave-on-insert-leave", { clear = true }),
	callback = function()
		if vim.bo.modified
			and not vim.bo.readonly
			and vim.fn.expand("%") ~= ""
			and vim.bo.buftype == ""
		then
			vim.cmd("silent! write")
		end
	end,
})

